package curvesdo;

/**
 *
 * @author mohamednagy
 */
public class Point {
    private int mX;
    private int mY;
    
    public Point(int x, int y){
        mX = x;
        mY = y;
    }

    public int getmX() {
        return mX;
    }

    public int getmY() {
        return mY;
    }

    public void setmX(int mX) {
        this.mX = mX;
    }

    public void setmY(int mY) {
        this.mY = mY;
    }
    
    
    
}
